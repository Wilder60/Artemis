using System;

public class Account
{

    private String EmailAddress;
    private String FirstName;
    private String LastName;


	public Account()
	{

	}

    public String EmailAddress
    {
        get => EmailAddress;
    }

    public String FirstName
    {
        get => FirstName;
    }

    public String LastName
    {
        get => LastName;
    }
}
